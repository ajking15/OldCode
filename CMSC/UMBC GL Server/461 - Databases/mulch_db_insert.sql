CREATE OR REPLACE VIEW summary_of_sellers (seller, num_shredded, num_nugget, num_black, num_red, total_sold, amount_due) AS
   SELECT seller, NVL(SUM(num_shredded),0) as shredded, NVL(SUM(num_nugget),0) as nugget, NVL(SUM(num_black),0) as black, NVL(SUM(num_red),0) as red,
   NVL(SUM(num_shredded),0) + NVL(SUM(num_nugget),0) + NVL(SUM(num_black),0) + NVL(SUM(num_red),0) AS total_sold,
   SUM(amount_due) AS amount_due
   FROM all_orders
   GROUP BY CUBE(seller)
   ORDER BY seller;