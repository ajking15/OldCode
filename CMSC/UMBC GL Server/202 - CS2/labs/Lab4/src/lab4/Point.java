public class Point {
		private int x;
		private int y;
		
		public Point(int x, int y) //constructor
		{
			this.x = x;
			this.y = y;
		}

		public int getX() { //accessor
			return x;
		}

		public int getY() { //accessor
			return y;
		}
		
}
